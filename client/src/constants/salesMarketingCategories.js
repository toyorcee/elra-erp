// Helper function to get categories based on type and transaction type
export const getCategories = (departmentType, transactionType) => {
  const categories =
    departmentType === "sales" ? SALES_CATEGORIES : MARKETING_CATEGORIES;
  return categories[transactionType] || [];
};

// Helper function to get all categories for a department
export const getAllCategories = (departmentType) => {
  const categories =
    departmentType === "sales" ? SALES_CATEGORIES : MARKETING_CATEGORIES;
  return [...categories.revenue, ...categories.expense];
};

